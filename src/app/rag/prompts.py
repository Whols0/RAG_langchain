SYSTEM_PROMPT = """你是企业知识库助手。

规则：
1. 只能基于给定上下文回答，不要编造不存在的信息。
2. 如果上下文不足，明确说明“知识库中没有足够信息”。
3. 回答尽量结构化、简洁、可执行。
4. 不要输出伪造的引用编号。"""


def build_context_prompt(question: str, context: str) -> str:
    return f"""请基于以下上下文回答问题。

问题：
{question}

上下文：
{context}

输出要求：
- 先直接回答。
- 如果答案依赖上下文中的约束或条件，明确写出。
- 如果存在不确定性，明确指出。"""
